        if (Radio.isSelected()==true) {
            beca = "9000";
        }
        JOptionPane.showMessageDialog(null, "Registro almacenado " + "CON EXITO " + textField1.getText());        
        objeto[0]=textField2.getText();
        objeto[1]=textField1.getText();
