    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
try {
    if (Lista.isEmpty()) {
        JOptionPane.showMessageDialog(null, "No hay registros guardados");
        return;
    }
    i--;
    if (i < 0) {
        i = Lista.size() - 1;
    }
    textField2.setText(Lista.get(i).Nombre);   // Nombre
    textField1.setText(Lista.get(i).NC); // Estatura
    textField3.setText(Lista.get(i).Beca);   // Peso
 
 

} catch (Exception x) {
    JOptionPane.showMessageDialog(null, "Error al retroceder: " + x.getMessage());
}
    
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
       try {
    if (Lista.isEmpty()) {
        JOptionPane.showMessageDialog(null, "No hay registros guardados");
        return;
    }
    i++;
    if (i >= Lista.size()) {
        i = 0;
    }
    textField2.setText(Lista.get(i).Nombre);   // Nombre
    textField1.setText(Lista.get(i).NC); // Numero ede control
    textField3.setText(Lista.get(i).Beca);   // Beca pa
 
 

} catch (Exception x) {
    JOptionPane.showMessageDialog(null, "Error al retroceder: " + x.getMessage());
}
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
      String beca = "800";
        if (Radio.isSelected()==true) {
            beca = "9000";
        }
        
       
        JOptionPane.showMessageDialog(null, "Registro almacenado " + "CON EXITO " + textField1.getText());        
        objeto[0]=textField2.getText();
        objeto[1]=textField1.getText();
        objeto[2]=beca;
        
        Tabla1.setValueAt(textField2.getText(), i, 0);
        Tabla1.setValueAt(textField1.getText(), i, 1);
        Tabla1.setValueAt(beca, i, 2);
        
        Lista.set(i, new Persona(textField2.getText(),textField1.getText().trim(),beca));
        
    }//GEN-LAST:event_jButton7ActionPerformed

